// Function to request STX from faucet
const requestTestnetSTX = async (address) => {
  try {
    console.log(`üí∞ Requesting testnet STX for ${address}...`);

    const response = await fetch('https://api.testnet.hiro.so/extended/v1/faucets/stx', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        address: address,
        stacking: false
      })
    });

    if (response.ok) {
      const result = await response.json();
      console.log(`‚úÖ Faucet request successful for ${address}`);
      return result;
    } else {
      console.log(`‚ö†Ô∏è  Faucet request failed for ${address}: ${response.statusText}`);
      return null;
    }
  } catch (error) {
    console.log(`‚ö†Ô∏è  Faucet request error for ${address}:`, error.message);
    return null;
  }
};

// Function to check account balance
const checkBalance = async (address) => {
  try {
    const response = await fetch(`${NETWORK.coreApiUrl}/extended/v1/address/${address}/balances`);
    const data = await response.json();
    return parseInt(data.stx.balance) / 1000000; // Convert from micro-STX
  } catch (error) {
    console.log(`Error checking balance for ${address}:`, error.message);
    return 0;
  }
};

